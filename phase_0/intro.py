# Why start here instead of jumping straight into code
"""
A common mistake beginners make is jumping straight into writing code without
understanding WHAT they're actually doing or WHY certain things work the way
they do. This leads to memorizing syntax without understanding concepts.

For example, without Phase 0 you might learn "python is an interpreted language"
in Phase 1 and just accept it - but WHY does that matter to you as a developer?
What's the alternative? What's a compiled language? Why does Python choose to
be interpreted?

This phase builds the foundation so that everything you learn after this
has context and makes logical sense, instead of being "magic rules to memorize."
"""