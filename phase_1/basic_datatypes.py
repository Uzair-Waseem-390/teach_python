# here we learn the basic datatypes


"""
1. int (integers) -> 15
2. float (floating point numbers) -> 15.99
3. str (strings) -> "Uzair43432432@#%%%$"
4. bool (boolean) -> True or False
"""

# int (integers)
age = 21
print("type of age is :", type(age))
print(age)

# float (floating point numbers)
age = 21.5
print("type of age is :", type(age))
print(age)

# str (strings)
age = "uzair"
print("type of age is :", type(age))
print(age)

# bool (boolean)
age = True
print("type of age is :", type(age))
print(age)